#pragma once

namespace lemons::dmx
{

[[nodiscard]] constexpr bool isValidChannel (int channel)
{
	return channel > 0 && channel <= 512;
}

[[nodiscard]] constexpr bool isValidChannelValue (int value)
{
	return value >= 0 && value <= 255;
}

}  // namespace lemons::dmx
